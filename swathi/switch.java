class switch1{
public static void main(String args[]){

 int month=4;
     String months="";
     switch(month){
     case 1:months="1.jan";
     break;
      case 2:months="2.feb";
     break;
      case 3:months="3.march";
     break;
      case 4:months="4.April";
     break;
      case 5:months="5.may";
     break;
      case 6:months="6.june";
     break;
      case 7:months="7.july";
     break;
     default:System.out.println("invalid month");
     }

     System.out.println(months);
     }
     }
